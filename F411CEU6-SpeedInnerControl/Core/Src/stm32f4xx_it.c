/**
  * @brief This function handles TIM1 update interrupt and TIM10 global interrupt.
  */
void TIM1_UP_TIM10_IRQHandler(void)
{
  /* USER CODE BEGIN TIM1_UP_TIM10_IRQn 0 */
  //1ms进入一次中断。
	MainEventCount++;
	test0=TIM1->CNT;
	mpu_dmp_get_data(&pitch,&roll,&yaw);
	test0=TIM1->CNT-test0;
	if(MainEventCount>=5)
	{
		MainEventCount=0;
		test1=TIM1->CNT;
		
		test1=TIM1->CNT-test1;
	}
	else if(MainEventCount==1)
	{	test2=TIM1->CNT;	
		AngleCalculate();			 //角度环计算函数，每5ms执行一次
		test2=TIM1->CNT-test2;
	}
	else if(MainEventCount==2)
	{	test3=TIM1->CNT;
		AngleControl();				 //角度环控制函数，每5ms执行一次
		test3=TIM1->CNT-test3;
	}
	else if(MainEventCount==3)
	{
		SpeedControlCount++;
		SpeedControlPeriod++;
    	if(SpeedControlCount >= 5)//25ms
    	{	test4=TIM1->CNT;
			getSpeed();			
      		SpeedControl();          //速度控制函数，每25ms调用一次
			SpeedControlOutput();
      		SpeedControlCount=0;
			SpeedControlPeriod=0;
			test4=TIM1->CNT-test4;
    	}
	}
	else if(MainEventCount==4)
	{   test6=TIM1->CNT;
		MotorOutput();	 		//电机输出函数，每5ms执行一次
		test6=TIM1->CNT-test6;
	}
  /* USER CODE END TIM1_UP_TIM10_IRQn 0 */
  HAL_TIM_IRQHandler(&htim1);
  /* USER CODE BEGIN TIM1_UP_TIM10_IRQn 1 */

  /* USER CODE END TIM1_UP_TIM10_IRQn 1 */
}

